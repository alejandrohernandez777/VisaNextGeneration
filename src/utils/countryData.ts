export const getCountryRiskScore = (countryCode: string): number => {
  const country = countryAssessmentLevels[countryCode];
  if (!country) return 0;

  // Convert assessment level to score (inverse relationship)
  switch (country.level) {
    case 1:
      return 20; // Low risk
    case 2:
      return 15; // Moderate risk
    case 3:
      return 10; // Higher risk
    default:
      return 5; // Unknown/Other
  }
};

export const getFinancialRequirements = (
  countryCode: string
): {
  baseAmount: number;
  additionalFunds: number;
} => {
  const country = countryAssessmentLevels[countryCode];
  const level = country?.level || 3;

  // Base amounts in AUD
  const baseAmounts = {
    1: 21000, // Level 1 countries
    2: 25000, // Level 2 countries
    3: 30000, // Level 3 countries
  };

  return {
    baseAmount: baseAmounts[level],
    additionalFunds: 8000, // Living costs per year
  };
};

